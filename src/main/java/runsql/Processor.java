package runsql;

import java.sql.ResultSet;
import java.sql.SQLException;

/**
 * Processes a ResultSet. Classes that implement this method must be thread safe as potentially
 * multiple job consumers can use the same Processor simultaneously.
 */
@FunctionalInterface
public interface Processor {
    void process(ResultSet resultSet) throws SQLException, ClassNotFoundException;
}
